package tesTCasesJava;

public class Swapingintegers {

	public static void main(String[] args) {

		int a = 5;
		int b = 4;
		
		System.out.println("The value of a is "+a+ " The value of b is "+b);
		
		a=a+b;//9
		b=a-b;//9-4
		a=a-b;
		System.out.println("The value of a is "+a+ " The value of b is "+b);

	}

}
